function modalBoxContent(num){
  return `
    <div id="myModal" class="modal">
      <!-- Modal content -->
      <div class="modal-content">
        <div class="modal-header">
          <span class="close">&times;</span>
          <h2>${data[num].name}</h2>
        </div>
        <div class="modal-body">
          <video width="80%" controls>
            <source src="${data[num].video}" type="video/mp4">
          </video>
        </div>
        <div class="modal-footer">
          <p>${data[num].detail}
          </p>
        </div>
      </div>
    </div>
  `;
}

function modalBox(){
  const modal = document.getElementById('myModal');
  const span = document.getElementsByClassName("close")[0];
  modal.style.display = "block";
// When the user clicks on <span> (x), close the modal
  span.onclick = function() {
    modal.style.display = "none";
    $('.modalBox').html('');// turn off sound of video
  };
  window.onclick = function(event) {
    if (event.target == modal) {
      modal.style.display = "none";
      $('.modalBox').html('');// turn off sound of video
    }
  };
}

function clickEvent(){
  $('.box').click(function( event ) {
  //testing value selecting
    console.log($(this).attr('value'));
    let num = parseInt($(this).attr('value'));
    $('.modalBox').html(modalBoxContent(num));
    console.log(modalBoxContent(num));
    modalBox();
  });
}

function renderResult(result,num) {
  return `
    <div class="box" value=${num}>
       <img src="${result.image}" alt="">
      <p>${result.name}</p>
    </div>`;
}

function displayYoutubeSearchData() {
  let results = [];
  for(let i = 0; i < data.length ; i++) {
    results.push(renderResult(data[i],i));}
  $('.js-search-results').html(results);
  $('.js-search-results').append(`<div class="modalBox"></div>`);
  clickEvent();
}

